function changeLanguage(language) {
    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        element.innerHTML = translations[language][key];
    });
}

document.addEventListener('DOMContentLoaded', function() {
    const brazilIcons = document.querySelectorAll('.icon-brazil');
    const usaIcons = document.querySelectorAll('.icon-usa');

    // Inicialização dos ícones
    brazilIcons.forEach(icon => icon.classList.add('color'));
    usaIcons.forEach(icon => icon.classList.add('bw'));

    function toggleLanguage(lang) {
        if (lang === 'pt') {
            brazilIcons.forEach(icon => {
                icon.classList.add('color');
                icon.classList.remove('bw');
            });
            usaIcons.forEach(icon => {
                icon.classList.add('bw');
                icon.classList.remove('color');
            });
            changeLanguage('pt');
        } else {
            usaIcons.forEach(icon => {
                icon.classList.add('color');
                icon.classList.remove('bw');
            });
            brazilIcons.forEach(icon => {
                icon.classList.add('bw');
                icon.classList.remove('color');
            });
            changeLanguage('en');
        }
    }

    brazilIcons.forEach(icon => {
        icon.addEventListener('click', function() {
            toggleLanguage('pt');
        });
    });

    usaIcons.forEach(icon => {
        icon.addEventListener('click', function() {
            toggleLanguage('en');
        });
    });
});